		<h2><li><U>Après la compétition</U> :</li></h2>

		<p>Il est primordial de se réhydrater par des eaux plates ou gazeuses riches en bicarbonates et en sodium (Vichy saint Yorre, Vichy Celestin, Badoit...). 
			 Il est tout aussi important de refaire les stocks de protéines, de glycogène, minéraux et vitamines, pour cela, il suffit de reprendre une alimentation équilibrée
			 comme lors de la période d’entrainement.</p>
				
