			<p>Le plus important est de ne jamais avoir une sensation de soif, il faut boire avant, pendant et après l’entrainement. 
			Si la sensation de soif apparait, l’organisme perd <mark>1 %</mark> du poids de corps mais aussi <mark>10 %</mark> de capacités physiques. 
			Il est impératif de s’hydrater tout au long de la journée :

			<ul>
				<li>Au réveil à jeun : 1 à 2 verres d’eau</li>
				<li>Pendant le petit déjeuner: 1 apport hydrique</li>
				<li>Dans la matinée : au moins ½ litre d’eau</li>
				<li>Pendant le déjeuner : 1 à 2 verres d’eau ou plus</li>
				<li>Dans l'après-midi : au moins ½ litre d’eau</li>
				<li>Pendant le Diner : 1 à 2 verres d’eau ou plus</li>
				<li>Au coucher : 1 à 2 verres d’eau (ou tisane)</li>
				<li>Pendant l’effort : l’équivalent de 150-200 ml d’eau répartis sur 20 min.</li>
			</ul>
		
