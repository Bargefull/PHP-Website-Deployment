		<p>Il faut privilégier les aliments facilitant la digestion pour éviter quelques troubles durant l’épreuve :</p>

		<ul>
			<li>En supprimant les graisses lors des repas comme la charcuterie, les fritures, le fromage, ...</li>
			<li>En supprimant les sucres (sodas, bonbons, jus de fruits,...)
			<li>En mangeant des viandes ou poissons maigres cuits à l’eau ou grillés
			<li>En diminuant les matières grasses sans supprimer les huiles riches en acides gras
			<li>En prenant le temps de bien mastiquer.
		</ul>

		<p>Il faut également penser à prendre le repas 3 heures avant la compétition pour une digestion en dehors de l’épreuve.</p>

